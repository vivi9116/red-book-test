function buildQuestions() {
  return prompts.map(([dimension, text], index) => ({
    id: `q${String(index + 1).padStart(2, "0")}`,
    dimension,
    text,
    options: optionKeysForQuestion(index).map((key, optionIndex) => ({
      id: String.fromCharCode(97 + optionIndex),
      text: optionTexts[key][index % 6],
      scores: { [key]: 1 },
      dimensionScore: 4 - optionIndex,
    })),
  }));
}

function optionKeysForQuestion(index) {
  const rotatingKeys = [
    ["emotion-debtor", "gentle-regulator"],
    ["boundary-overloader", "silent-burner"],
    ["emotion-debtor", "boundary-overloader"],
    ["silent-burner", "gentle-regulator"],
  ];

  return ["overthinking-spiral", "perfect-controller", ...rotatingKeys[index % rotatingKeys.length]];
}

